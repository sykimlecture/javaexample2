package java0404;

public class Person {
    String name;
    float height;
    float weight;

    public Person() {

    }

    public Person(String pName) {
        name = pName;
    }

    public Person(String pName, float pHeight, float pWeight) {
        name = pName;
        height = pHeight;
        weight = pWeight;
    }
}
